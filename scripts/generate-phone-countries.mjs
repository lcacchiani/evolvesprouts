#!/usr/bin/env node
/**
 * Generate shared phone country metadata for admin_web and public_www.
 * Reads libphonenumber-js `metadata.min.json` (same source as backend phonenumbers).
 */
import { readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { dirname, join } from "node:path";
import { createRequire } from "node:module";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, "..");
const require = createRequire(import.meta.url);

function resolveMetadataPath() {
  const candidates = [
    join(repoRoot, "apps/admin_web/node_modules/libphonenumber-js/metadata.min.json"),
    join(repoRoot, "apps/public_www/node_modules/libphonenumber-js/metadata.min.json"),
  ];
  for (const p of candidates) {
    if (existsSync(p)) {
      return p;
    }
  }
  try {
    return require.resolve("libphonenumber-js/metadata.min.json", {
      paths: [join(repoRoot, "apps/admin_web"), join(repoRoot, "apps/public_www")],
    });
  } catch {
    throw new Error(
      "Could not find libphonenumber-js/metadata.min.json. Run npm install in apps/admin_web or apps/public_www.",
    );
  }
}

const metadataPath = resolveMetadataPath();

/** ~30 high-signal regions for Evolve Sprouts; remainder follow alphabetically. */
const PREFERRED_REGION_ORDER = [
  "HK",
  "CN",
  "MO",
  "TW",
  "SG",
  "MY",
  "TH",
  "VN",
  "ID",
  "PH",
  "JP",
  "KR",
  "IN",
  "AU",
  "NZ",
  "GB",
  "US",
  "CA",
  "DE",
  "FR",
  "IT",
  "ES",
  "NL",
  "BE",
  "CH",
  "AE",
  "SA",
  "ZA",
  "PT",
  "IE",
];

const raw = readFileSync(metadataPath, "utf8");
const metadata = JSON.parse(raw);
const countryEntries = metadata.countries;
if (!countryEntries || typeof countryEntries !== "object") {
  throw new Error("metadata.min.json: missing countries object");
}

const allRegions = Object.keys(countryEntries).sort();
const preferredSet = new Set(
  PREFERRED_REGION_ORDER.filter((r) => allRegions.includes(r))
);
const rest = allRegions.filter((r) => !preferredSet.has(r));
const ordered = [
  ...PREFERRED_REGION_ORDER.filter((r) => allRegions.includes(r)),
  ...rest,
];

// English names only (shared across locales); localized labels would need per-locale generated tables.
const displayNames = new Intl.DisplayNames(["en"], { type: "region" });

const rows = ordered.map((region) => {
  const entry = countryEntries[region];
  const dialCode = Array.isArray(entry) ? String(entry[0]) : "";
  let englishName;
  try {
    englishName = displayNames.of(region) || region;
  } catch {
    englishName = region;
  }
  return { region, dialCode, englishName };
});

const header = `/* Auto-generated by scripts/generate-phone-countries.mjs — do not edit by hand. */

export const PHONE_COUNTRIES = ${JSON.stringify(rows, null, 2)} as const;

export type PhoneCountryRow = (typeof PHONE_COUNTRIES)[number];
`;

const targets = [
  join(repoRoot, "apps/admin_web/src/lib/phone-countries.generated.ts"),
  join(repoRoot, "apps/public_www/src/lib/phone-countries.generated.ts"),
];

for (const path of targets) {
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, header, "utf8");
}

console.log(
  `Wrote ${targets.length} phone country files (${rows.length} regions) from ${metadataPath}.`
);
